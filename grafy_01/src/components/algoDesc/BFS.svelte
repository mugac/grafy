        <p>BFS (Breadth-First Search) je algoritmus pro procházení grafu nebo jiného datového struktury. Jeho principem je procházet graf šířkou, tedy procházet všechny uzly přednostně ve stejné úrovni, než se přesuneme do další úrovně.

            BFS používá frontu (queue) k uložení uzlů, které ještě nebyly prozkoumány. Algoritmus postupně vytahuje uzly z fronty a prochází jejich sousedy. Pokud uzel ještě nebyl prozkoumán, přidá se do fronty a označí se jako prozkoumaný.
            
            BFS můžeme použít k nalezení nejkratší cesty mezi dvěma uzly v grafu nebo k prohledání celého grafu.BFS je efektivní v případech, kdy hledáme nejkratší cestu mezi dvěma uzly. Je ale pomalejší než DFS (Depth-First Search), pokud chceme projít celý graf</p>
    
